function change() {
    switch (document.getElementById("color").value) {
        case "red": document.getElementById("doc").style.backgroundColor = "red";
            break;
    }
    switch (document.getElementById("color").value) {
        case "blue": document.getElementById("doc").style.backgroundColor = "blue";
            break;
    }
    switch (document.getElementById("color").value) {
        case "yellow": document.getElementById("doc").style.backgroundColor = "yellow";
            break;
    }
}